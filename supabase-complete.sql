-- Supabase SQL Script for Belarusian Universities Data
-- Tables: universities, faculties, institutes, specialties

-- Drop existing tables (in correct order due to foreign keys)
DROP TABLE IF EXISTS specialties;
DROP TABLE IF EXISTS institutes;
DROP TABLE IF EXISTS faculties;
DROP TABLE IF EXISTS universities;

-- Create universities table
CREATE TABLE universities (
    id TEXT PRIMARY KEY,
    short_name TEXT NOT NULL,
    full_name TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Create faculties table
CREATE TABLE faculties (
    id TEXT PRIMARY KEY,
    university_id TEXT NOT NULL REFERENCES universities(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    code TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Create institutes table
CREATE TABLE institutes (
    id TEXT PRIMARY KEY,
    university_id TEXT NOT NULL REFERENCES universities(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    code TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Create specialties table
CREATE TABLE specialties (
    id TEXT PRIMARY KEY,
    university_id TEXT NOT NULL REFERENCES universities(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    code TEXT,
    faculty_id TEXT REFERENCES faculties(id) ON DELETE SET NULL,
    institute_id TEXT REFERENCES institutes(id) ON DELETE SET NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

